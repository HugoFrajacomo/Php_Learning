<?php
    // Auto incremento
    $a = 1;
    echo $a++; // 1
    echo "<br>";
    echo $a; // 2
    echo "<br>";
    echo ++$a; // 3
    echo "<br>";
    echo $a; // 3

    echo "<br>";
    $b = 10;
    echo $b--; // 10
    echo "<br>";
    echo $b; // 9

    /*
    +=  -> Adiciona um valor a variável
    -=  -> Subtrai um valor a variável
    *=  -> Multiplica um valor a variável
    /=  -> Divide um valor a variável
    %=  -> Resto da divisão
    */
    echo "<br>";
    $f = 0;
    $f += 10; // $f = $f + 10
    echo $f;